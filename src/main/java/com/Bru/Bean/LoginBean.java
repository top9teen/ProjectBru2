package com.Bru.Bean;

public class LoginBean {
	private int loId;
	private String loEmail;
	private String loPassword;
	private String loStatus;
	public int getLoId() {
		return loId;
	}
	public void setLoId(int loId) {
		this.loId = loId;
	}
	public String getLoEmail() {
		return loEmail;
	}
	public void setLoEmail(String loEmail) {
		this.loEmail = loEmail;
	}
	public String getLoPassword() {
		return loPassword;
	}
	public void setLoPassword(String loPassword) {
		this.loPassword = loPassword;
	}
	public String getLoStatus() {
		return loStatus;
	}
	public void setLoStatus(String loStatus) {
		this.loStatus = loStatus;
	}
	
	
}
